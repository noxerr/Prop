        public void afegir_dades_taula(Vector<String> info) {
        if (events.contains(info.get(0))) {
            Missatge_basic mb = new Missatge_basic(this, true);
            mb.setMsg("Event a existeix");
            mb.setVisible(true);
            mb.pack();
        }
        else {
            DefaultTableModel model;
            model = (DefaultTableModel) jTable1.getModel();
            model.addRow(info);
            events.add(info.firstElement());
        }
       
    }
     

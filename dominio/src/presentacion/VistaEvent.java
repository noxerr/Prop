    private int TipusToInt(String tipus) {
        /* tipusEvent = {0,1,2,3,4} equivalent a {reunio, esport, votacio, dinar, conferencia}.*/
        if (tipus.equals("Reunio")) return 0;
        else if (tipus.equals("Esport")) return 1;
        else if (tipus.equals("Votacio")) return 2;
        else if (tipus.equals("Dinar")) return 3;
        else if (tipus.equals("Conferencia")) return 4;
        return -1; // cas d'error, mai es pot donar
    }
    
    
